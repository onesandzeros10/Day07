#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	while (src[i] != '\0')
	{
		dest[j] = src[i];
		j++;
		i++;
	}
	dest[j] = '\0';
	return (dest);
}

int		ft_strlen(char *str)
{
	int len;

	len = 0;
	while (*str != '\0')
	{
		len++;
		str++;
	}
	return (len);
}

char	*ft_strdup(char *src)
{
	char *duplicate;

	duplicate = (char*)malloc(sizeof(*duplicate) * (ft_strlen(src) + 1));
	duplicate = ft_strcpy(duplicate, src);
	return (duplicate);
}
